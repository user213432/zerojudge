#include <iostream>
using namespace std;
int main()
{
    int n;
    int x[101];
    int sum=0;
    cin>>n;
    for(int i=0; i<n; i++)
    {
        cin>>x[i];
    }
    for(int i=0; i<n; i++)
    {
        if(x[i]==0)
        {
            if(i==0)
            {
                sum+=x[1];
            }
            else if(i==n-1)
            {
                sum+=x[n-2];
            }
            else//(x[i-1]<x[i+1])
            {
                if(x[i-1]<x[i+1])
                {
                   sum+=x[i-1]; 
                }
                else
                {
                    sum+=x[i+1];
                }
            }
            
        }
    }
    cout<<sum;
}
